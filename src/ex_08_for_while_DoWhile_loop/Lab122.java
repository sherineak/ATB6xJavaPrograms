package ex_08_for_while_DoWhile_loop;

public class Lab122 {
    public static void main(String[] args) {
        int i = 10;
        do{
            System.out.println(i);
            i++;
        }while (i<10);                  // print and checking condition




        while(i<10){
            System.out.println(i);    // checking condition and print
            i++;
        }
    }
}
