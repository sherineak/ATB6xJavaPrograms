package ex_08_for_while_DoWhile_loop;

public class Lab121 {
    public static void main(String[] args) {

       // While -->   initialization  -> condition --> body --> increment or decrement

        // Do while --> initialization -> body --> increment or decrement  -> condition

        int i = 0;
        do{
            System.out.println(i);

            i--;
        }while (i>-5);

    }
}
