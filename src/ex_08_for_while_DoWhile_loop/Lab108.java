public class Lab108 {
    public static void main(String[] args) {

        for(int i = 0; i < 10; i++){     // 10 times will execute this statement
            System.out.println("Sherine");
        }

        for(int i = 1; i <= 10; i++){   // 10 times will execute this statement
            System.out.println("Sherine");
        }

        //both for loops are same
    }
}
