package ex_27042024;

public class Lab115 {
    public static void main(String[] args) {
        // while loop
        // A -> Initialization
        //B -> Condition
        // c -> increment , decrement or updation

        int i =1;    // A -> initialization
        while(i<=10){  // B -> condition
            System.out.println(i);
            i++;    // c -> increment
        }

        for(int j =1; j<=10;j++){
            System.out.println(j);   // both while loop  and for loop is same
        }
    }
}
