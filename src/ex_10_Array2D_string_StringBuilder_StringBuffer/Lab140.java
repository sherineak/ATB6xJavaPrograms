public class Lab140 {
    public static void main(String[] args) {
        int num = 3;

        for(int i =1; i <= num;i++){
            for (int j =1; j <= i; j++){
                System.out.print("*");   // It's a print not a next line
            }
            System.out.println(" ");   //  To display in next line
        }


    }
}
